'''Aqui iremos fazer um contador automatico até o numero 10, começando do 0.
while (enquanto)'''

contador = 0   # -> Variavel recebe o valor inicial de 0

while contador <=10: # -> enquando meu contador for igual ou menor do que 10 ele vai executar o código!
    print(contador) # -> vou exibir o valor do contador que inicialmente colocamos como 0
    contador = contador + 1 # -> Toda vez que nosso bloco de código executar essa linha o contador vai receber o valor anterior + 1, colocando um novo valor na variavel. 
print('Acabouu!') # -> quando o contador chegar até o valor de 10 ele para de executar o código e exibe essa mensagem